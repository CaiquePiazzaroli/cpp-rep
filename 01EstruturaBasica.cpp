// Importando a blibioteca para usar input e output
#include <iostream> 

// Usando o namespace std
using namespace std;

// Função main: tudo dentro dela será executado
int main() {

	// Cout é um objeto da blibioteca iostream para exibir mensagens no console
	// endl no fim da frase faz o programa pular de linha
    cout << "Hello World!" << endl;
    
    // \n também querba a linha
    cout << "My program. \n";
    
    // Pausa o sistema e não fecha a tela
    system("pause");
    
    // Finaliza a função
    return 0;
}
